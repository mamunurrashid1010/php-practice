    <?php
        /**
         * This is example of php switch condition.
         */

        # switch condition example
        echo "<h4>Switch condition example:</h4>";
        $color="white";
        switch ($color){
            case "red":
                echo "The value is: " .$color;
                break;
            case "green":
                echo "The value is: " .$color;
                break;
            case "blue":
                echo "The value is: " .$color;
                break;
            case "white":
                echo "The value is: " .$color;
                break;
            case "black":
                echo "The value is: " .$color;
                break;
        }


    ?>

